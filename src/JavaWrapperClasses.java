public class JavaWrapperClasses {
    public static void main(String[] args){
        // wrapper classes

        /*A Wrapper class in Java is a class that wraps around
         a primitive data type and converts it into an object.
          These classes allow you to treat primitive data types
           (such as int or double) as objects. */
        Integer i = Integer.valueOf(12);
        System.out.println(i);

    }
}
